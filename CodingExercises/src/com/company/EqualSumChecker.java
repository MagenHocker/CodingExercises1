package com.company;

public class EqualSumChecker {

  /**
   * determines if the sum of i and j are equal to k
   * @param i represents an int
   * @param j represents an int
   * @param k represents an in
   * @return ture if and only if i and j add to k
   */
  public static boolean hasEqualSum(int i, int j, int k) {
    return (i + j) == k;
  }

}
